/*--------------------------------
	直接选择排序
----------------------------------
*/

#include <stdio.h>
void main()
{
	int a[10] = {1,2,3,4,5,9,7,8,6,10};
	int i, j, k, m;
	for (i = 0; i < 9; i++)
	{
		k = i;
		for (j = i+1; j < 10; j++)
		{
			if (a[k] < a[j])
			{
				k = j;
			}
		}
		if (k != i)
		{
			m = a[i];
			a[i] = a[k];
			a[k] = m;
		}
	}
	
	for (i = 0; i < 10; i++)
	{
		printf("%3d",a[i]);
	}
}
