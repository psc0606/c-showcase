/*----------------------------------------
**	1.自守数是指一个数的平方的尾数等于该数自身数。
**	例如：25^2 = 625	76^2 = 5776	
**	9376^2 = 87909376
**	求出200 000以内的自守数。
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	long long i, j, k, n ,m = 0;
	for (i = 1; i <= 200000; i++)
	{
		k = i;
		j = 1;
		while (k = k / 10)
		{
			j++;
		}
		for (n = 1; j > 0; j--)
		{
			n *= 10;
		}
		if ( i * i % n== i)
		{
			m++;
			printf("%lld^2 = %lld\n",i,i*i);
		}
	}
	printf("num = %d\n",m);
}
