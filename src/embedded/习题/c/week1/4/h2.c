/*----------------------------------------
**	一个数如果恰好等于它的因子之和，这就是完数；
**	6=1+2+3；编写程序，找出1000以内的所有完数
**	（每一个因子只使用一次）
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i, j, sum;
	for (i = 1; i < 1000; i++)
	{
		sum = 0;
		for (j = 1; j < i; j++)
		{
			if (i % j == 0)
			{
				sum += j;
			}
		}
		if (sum == i)
		{
			printf("%d  ",i);
		}
	}
}
