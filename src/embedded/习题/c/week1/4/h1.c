/*----------------------------------------
**	打印出所有的“水仙花数”，所谓的“水仙花数”是
**	指一个三位数,其各位数字立方和等于该数本身
**	 153=1³+5³+3³
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i, j, k, m, n=0;
	for (i = 100; i < 1000; i++)
	{
		j = i % 10;
		k = i % 100 / 10;
		m = i / 100;
		n = j * j * j + k * k * k + m * m * m; 
		if (n == i)
		{
			printf("%d\n",i);
		}
	}
}
