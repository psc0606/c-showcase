/*----------------------------------------
**	 3. 设N是一个四位数，它的9倍是其反序，求N。
**	反序数就是将整数的数字倒过来形成的整数。
**	例如：1234的反序数4321
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i, j, k;
	for (i = 1000; i < 10000; i++)
	{
		j = i/1000 + i/100%10*10 + i/10%10*100 + i%10*1000;
		if (9 * i == j)
		{
			printf("%d\n",i);
		}
	}
}
