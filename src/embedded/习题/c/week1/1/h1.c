/*----------------------------------------
**	1. .输入一句英文，将其中小写字母全换为
**	大写字母后输出。
**----------------------------------------
*/

#include <stdio.h>
int main()
{
	int i;
	char A[100];
	printf("请输入要转换的字母！\n");
	gets(A);
	for(i = 0;A[i] !='\0';i++)
		{
			if(A[i] >= 'a'&& A[i] <= 'z')
				{
					A[i] = A[i]-32;
				}
			printf("%c",A[i]);
		}
	return 0;
}
