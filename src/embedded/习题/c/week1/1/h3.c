/*----------------------------------------
**	输入一句英文，统计其中大小写字母，单词个数
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i,j,k,n = 0,N = 0,num = 0;
	char A[100];
	printf("请输入一句英文！\n");
	gets(A);
	for(i = 0;A[i] != '\0';i++)
		{
			if(A[i] >= 'A'&&A[i] <= 'Z')
				{
					N++;
				}
			else if (A[i] >= 'a'&&A[i] <= 'z')
				{
					n++;
				}
			else if(A[i] == ' ')
				{
					num++;
				}
		}
	printf("大写字母个数为:%d,小写字母个数为:%d,单词数为:%d\n",N,n,num+1);
}
