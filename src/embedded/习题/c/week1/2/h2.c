/*----------------------------------------
**	 2、 从键盘输入任意一个十进制的整数分别
**	 转换成二进制，八进制和十六进制的形式
**	并分别输出。
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i,j,k=0,n[20];
	printf("请输入一个数:\n");
	scanf("%d",&i);
	for(j=i;j != 0;k++)
	{
		n[k]=j%2;
		j = j/2;
	}
	for(k--;k>=0;k--)
	{
		printf("%d",n[k]);
	}
	printf("\n");
	printf("%#o,%#x\n",i,i);
}
