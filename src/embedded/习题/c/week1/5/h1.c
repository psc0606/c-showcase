/*----------------------------------------
**	试图讲一个任意的正整数分解质因数
**	例如：输入90，打印90=2*3*3*5.
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i, j = 0, k, n;
	printf("请输入一个正数:");
	scanf("%d",&n);
	printf("%d = ",n);
	for (k = n, i = 2; k != 1;)
	{
		if (k % i == 0)
		{
			j++;
			if (j > 1)
			{
				printf("*");
			}
			k = k / i;
			printf("%d",i);
		}
		else 
		{
			i++;
		}
	}
}
