void main()
{
	int i, j, x, y, k = 0;
	for (x = 0; x <= 3; x++)
	{
		for (y = 0; y <= 4; y++)
		{
			i = 5 * x + 3 * y;
			if (i >= 3 && i <= 27)
			{
				printf("x =%d,y = %d,i =%d\n",x,y,i);
				k++;
			}
		}
	}
	printf("number = %d\n",k);
}
