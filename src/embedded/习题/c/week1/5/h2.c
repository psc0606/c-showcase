/*----------------------------------------
**	有五本书，要借给A，B，C三个小朋友，若没
**	人每次只能借一本,则可以有多少种不同的借法？
**	打印出不同的借法。
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i, j, k, n = 0;
	for (i = 1; i <= 5; i++)
	{
		for (j = 1; j <= 5; j++)
		{
			for (k = 1; k <= 5; k++)
			{
				if (i != j && i != k && j != k)
				{
					printf("%d,%d,%d\n",i,j,k);
					n++;
				}
			}
		}
	}
	printf("number = %d",n);
}
