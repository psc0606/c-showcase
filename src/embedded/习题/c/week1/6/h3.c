/*----------------------------------------
**	3. 一条长阶梯，若每步跨2阶，则最后剩下1阶
**	若每步跨3阶，则最后剩下2阶,
**	若每步跨5阶，则最后剩下4阶
**	若每步跨6阶则最后剩下5阶
**	若每步跨7阶则最后剩下0阶
**	这条阶梯共有多少阶？
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int i;
	for (i = 7; ; i += 7)
	{
		if (i % 7 == 0)
		{
			if (i % 6 == 5)
			{
				if (i % 5 == 4)
				{
					if (i % 3 == 2)
					{
						if (i % 2 == 1)
						{
							printf("%d\n",i);
							break;
						}
					}
				}
			}
		}
	}
}
