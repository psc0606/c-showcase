/*----------------------------------------
**	4. 有30个人，其中有男人，女人，小孩，在一
**	家饭馆吃饭共花了50块，每个男人花3块,
**	每个女人花2块，小孩花1块,问男人，女人，
**	小孩各有多少人?打印出所有的可能性
**----------------------------------------
*/

#include <stdio.h>
void main()
{
	int x, y, z, n = 0;
	for (x = 1; x < 17; x++)
	{
		for (y = 1; y < 25; y++)
		{
			for (z = 1; z < 30; z++)
			{
				if (3*x + 2*y + z == 50 && x + y + z == 30)
				{
					printf("x = %d, y = %d, z = %d\n",x,y,z);
					n++;
				}
			}
		}
	}
	printf("num = %d\n",n);
}
