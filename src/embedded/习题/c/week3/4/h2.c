/*----------------------------------------
**	2.	从键盘输入一个整形数字n，在终端打印如下
**（例N=4）：
   1 2 3 4
   2 3 4 1
   3 4 1 2
   4 1 2 3
**----------------------------------------
*/

#include <stdio.h>

void main()
{
	int i, j, k, n;
	printf("input n = ");
	scanf("%d",&n);
	for (i = 0; i < n; i++)
	{
		for (j = 0; j < n; j++)
		{
			k = j + i + 1;
			if (k > n)
			{
				k %= n;
			}
			printf("%3d",k);
		}
		printf("\n");
	}
}
